# include <stdio.h>

int main(void) {
	printf("\nHello, World!\n\n");
	printf("Integer: %d\n",32); // integer
	printf("Large Integer: %ld\n",3734763497655954745); // large integer
	printf("Float: %f\n",42.765); // float
	printf("Large Float: %lf\n",32.348658473563878568568865858); // large float
	printf("Single Character: %c\n",'D'); // single character
	printf("String: %s\n","It's me!"); // string
	printf("Hexadecimal: %x\n",16); // hexadecimal
  return 0;
}
